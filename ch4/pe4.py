def investmentChart3():
    # Create the graphics window
    win = GraphWin("Investment Growth Chart", 600, 500)
    win.setBackground("white")
    win.setCoords(-1.75, -200, 11.5, 10400)
    Text(Point(8,10000),"Initial investment:").draw(win)
    Text(Point(8,9000), "         Interest:").draw(win)
    inputMoney = Entry(Point(10, 10000), 5)
    inputMoney.setText("0.0")
    inputMoney.draw(win)
    inputInterest = Entry(Point(10, 9000), 5)
    inputInterest.setText("0.0")
    inputInterest.draw(win)
    Text(Point(-1,0), '0.0K').draw(win)
    Text(Point(-1,2500), '2.5K').draw(win)
    Text(Point(-1,5000), '5.0K').draw(win)
    Text(Point(-1,7500), '7.5K').draw(win)
    Text(Point(-1,10000), '10.0K').draw(win)

    win.getMouse()
    inputMoney = float(inputMoney.getText())
    inputInterest = float(inputInterest.getText())
    # Draw bar for initial investment
    bar = Rectangle(Point(0,0), Point(1, inputMoney))
    bar.setFill("green")
    bar.setWidth(2)
    bar.draw(win)
    # Draw bars for subsequent years
    for year in range(1,11):
        inputMoney = inputMoney * (1+inputInterest)
        bar = Rectangle(Point(year, 0), Point(year+1, inputMoney))
        bar.setFill("green")
        bar.setWidth(2)
        bar.draw(win)
    input("Press <Enter> to quit")
    win.close()

def circleIntersect():
    inputR = float(input("Enter the radius of the circle: "))
    inputY = float(input("Enter the y intercept: "))
    win = GraphWin("Circle Intersect", 600,600)
    win.setCoords(-10, -10, 10, 10)
    circle = Circle(Point(0,0), inputR)
    circle.draw(win)
    line = Line(Point(-10,inputY), Point(10, inputY))
    line.draw(win)
    iX = abs(math.sqrt(inputR**2 - inputY**2))
    intersect1 = Point(iX, inputY)
    intersect1.setFill("red")
    intersect1.setOutline("red")
    intersect1.draw(win)
    intersect2 = Point(-iX, inputY)
    intersect2.setFill("red")
    intersect2.setOutline("red")
    intersect2.draw(win)
    win.getMouse()

